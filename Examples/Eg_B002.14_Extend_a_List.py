# Extend

# Create a list of my friends
my_friends = ['Yoseph', 'Eden', 'Haymanot']

# Print the list
print('My old friends are:', my_friends)

# Create a list of my new friends
new_friends = ['Yewub', 'Ribka']

# Print the list
print('My new friends are:', new_friends)

# Append the list of my new friends to my friends
my_friends.extend(new_friends)

# print my friends
print('Now this are all my friends:', my_friends)

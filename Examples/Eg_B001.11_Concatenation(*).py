# Concatenation (*)
name = 'Daniel'
f_name = 'Yohannes'
"""
name * x
Return a new string by multiplying the string with x
"""

new_name = name * 3
new_name_1 = f_name * len(name)

print(name)  # Output: Daniel
print(new_name)  # Output: DanielDanielDaniel
print(f_name)  # Output: Yohannes
print(new_name_1)  # Output: YohannesYohannesYohannesYohannesYohannesYohannes

# The in Operator

name = 'Daniel'
f_name = 'Yohannes'

print('D' in name)  # Output: True
print('D' in f_name)  # Output: False
print('d' in name)  # Output: False
print('yoha' in f_name)  # Output: False
print('Yoha' in f_name)  # Output: True
print('iel' in name)  # Output: True
print('nnas' in f_name)  # Output: False

# Count

# Create a list of randomly repeated letters
rand = list('abnsjkokdjsjaodyuiewioksanajnhhfajkljkasuihwmkankds')

# Print the list
print(rand)  # Output: ['a', 'b', 'n', 's', 'j', 'k', 'o', 'k', 'd', 'j', 's', 'j', 'a', 'o', 'd', 'y', 'u', 'i', 'e',
# 'w', 'i', 'o', 'k', 's', 'a', 'n', 'a', 'j', 'n', 'h', 'h', 'f', 'a', 'j', 'k', 'l', 'j', 'k',
# 'a', 's', 'u', 'i', 'h', 'w', 'm', 'k', 'a', 'n', 'k', 'd', 's']

# Count the occurrence of 'a' in the list and print
print('\'a\' was listed ', rand.count('a'), 'times in this list.')  # Output: 'a' was listed  7 times in this list.

# Count the occurrence of 'h' in the list and print
print('\'h\' was listed ', rand.count('h'), 'times in this list.')  # Output: 'h' was listed  3 times in this list.
